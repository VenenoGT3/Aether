// Helper function to read cookie value
export function getClientLocale(): Locale {
  if (typeof window === "undefined") return "en";
  const match = document.cookie.match(/aether-lang=(en|it)/);
  if (match) return match[1] as Locale;
  return (localStorage.getItem("aether-lang") as Locale) || "en";
}

// Helper function to write cookie value
export function setClientLocale(locale: Locale) {
  if (typeof window !== "undefined") {
    localStorage.setItem("aether-lang", locale);
    document.cookie = `aether-lang=${locale}; path=/; max-age=31536000; SameSite=Lax`;
    window.dispatchEvent(new Event("locale-change"));
  }
}

// React Hook for Client-side components
export function useTranslation() {
  const [locale, setLocale] = useState<Locale>("en");

  useEffect(() => {
    // eslint-disable-next-line react-hooks/set-state-in-effect -- sync persisted locale on mount
    setLocale(getClientLocale());
    const handleLocaleChange = () => {
      setLocale(getClientLocale());
    };
    window.addEventListener("locale-change", handleLocaleChange);
    return () => window.removeEventListener("locale-change", handleLocaleChange);
  }, []);

  const t = (text: string): string => {
    if (locale === "it") {
      return dictionary[text] || text;
    }
    return text;
  };

  return { 
    t, 
    locale, 
    setLocale: (newLocale: Locale) => {
      setClientLocale(newLocale);
      setLocale(newLocale);
    } 
  };
}

